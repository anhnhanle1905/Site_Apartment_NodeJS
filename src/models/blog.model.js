import mongoose from "mongoose";

const blogSchema = new mongoose.Schema({
  image: {
    type: String,
    trim: true,
    required: true,
  },
  title: {
    type: String,
    trim: true,
    required: true,
  },
  time: {
    type: String,
    trim: true,
    required: true,
  },
  description: {
    type: String,
    trim: true,
    required: true,
  },
});

const Blog = mongoose.model("Blogs", blogSchema);
export default Blog;
